finalVelocity = int(input("Enter the v of the trapezium "))
initialVelocity = int(input("Enter the u of the trapezium "))
height = int(input("Enter the a of the trapezium "))
displacement = (finalVelocity ** 2 - initialVelocity ** 2) / (2 * height)
print("Displacement of the Trapezium is ", int(displacement))



