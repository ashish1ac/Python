"""
Consider day 1 as the Sunday and day 7 as the Saturday
"""

day_number = int(input("Enter the number of the Day: "))

if day_number == 1:
    print("It's Sunday!!")
elif day_number == 2:
    print("It's Monday!!")
elif day_number == 3:
    print("It's Tuesday!!")
elif day_number == 4:
    print("It's Wednesday!!")
elif day_number == 5:
    print("It's Thursday!!")
elif day_number == 6:
    print("It's Friday!!")
elif day_number == 7:
    print("It's Saturday!!")
else:
    print("Please enter a day number between 1 to 7, Thank You!!")
