"""
Write a function to replace all occurrences of the character 'e' with 'i' in the string "experience". Print the modified string.
Same problem but replace only 1 e.
"""

s = 'experience'

print(s.replace('e', 'i'))  # Replace all the occurrences
print(s.replace('e', 'i', 1))  # Replace one 1 occurrence
